public class Logical {
    public static void main(String[] args) {
        boolean val1 = true;
        boolean val2 = false;
        boolean val3 = true;


//        AND operator(&&) -> returns true if both?all conditions are true
        System.out.println(val1 && val2 && val3);

//        OR operator(||) -> return True if any one of the is true
        System.out.println(val1 || val2 || val3);

//        NOR operator (!) - reverse things
        System.out.println(!val2);







    }
}
